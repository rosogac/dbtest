SET NOCOUNT ON;
DECLARE @RowCount INT
DECLARE @Days INT
DECLARE @Date DATE

SET @RowCount = 0
SET @Days = -365

BEGIN TRANSACTION

WHILE @RowCount < 10

BEGIN

SET @Date = DATEADD(DAY,@Days,GETDATE())

	INSERT INTO PRICING_APPLICATION..PR_PRICE
	(
	ITEM_ID,
	PRICE_DATE,
	PRICE_VALUE
	)
		(SELECT ITEM_ID
		, @Date
		, RAND(CHECKSUM(NEWID()))*100
		FROM ITEM_CATALOGUE..IC_ITEM
		 )

SET @Days = @Days + 1
SET @RowCount = @RowCount + 1


END

COMMIT
